# =====================================================================
# 文案治理：白名单 / 黑名单 / 语义冲突
# =====================================================================

PROMO_BLACKLIST = [
    "买一送一", "买二送一", "顺丰包邮", "限时特惠", "今日秒杀",
    "正品险", "官方补贴", "领券立减", "秒杀价", "今日必抢"
]

FLAVOR_CONFLICTS = {
    "不辣":    ["爆辣", "香辣", "麻辣", "辣而不燥", "微辣"],
    "微辣":    ["爆辣", "麻辣"],
    "韩式甜辣": ["藤椒", "麻辣", "五香"],
    "非油炸":   ["油炸", "酥脆"],
}


def check_copy(copy_text, flavor):
    """文案合规检查，返回违规词列表"""
    violations = []
    for promo in PROMO_BLACKLIST:
        if promo in copy_text:
            violations.append(f"促销词违规：{promo}")
    for conflict in FLAVOR_CONFLICTS.get(flavor, []):
        if conflict in copy_text:
            violations.append(f"口味冲突：{flavor} vs {conflict}")
    return violations
