CATEGORY_BANS = {
    "luwei": BANS_LUWEI,           # 卤水 / 调味料 / 卤味熟食
    "noodle": BANS_NOODLE,         # 方便面 / 火鸡面 / 拌面 / 速食面
    "snack": BANS_SNACK,           # 即食零食 / 魔芋 / 毛肚 / 肉脯
    "bakery": BANS_BAKERY,         # 烘焙 / 蛋糕 / 饼干
    "general": [],                 # 通用（只用 COMMON）
}


def get_bans(category, flavor=""):
    """取出指定品类的完整禁项清单（common + category）"""
    cat = CATEGORY_BANS.get(category.lower(), [])
    all_bans = BANS_COMMON + cat
    return [b.replace("{flavor}", flavor or "指定口味") for b in all_bans]


# =====================================================================
# 食欲 6 参数模板（挂汁 / 油亮 / 堆叠 / 配料 / 热气 / 拉丝）
# =====================================================================

APPETITE_KEYS = ["挂汁感", "油亮感", "堆叠感", "配料感", "热气感", "拉丝感"]

APPETITE_MAP = {
    "挂汁感": {
        "高": "酱体浓稠附着食材表面，高光明显，挂汁区域 60%+",
        "中": "适度挂汁，酱体均匀覆盖",
        "低": "清淡少汁，仅局部点缀",
    },
    "油亮感": {
        "高": "食材表面油润反光强烈，高光点清晰",
        "中": "适度油光，自然反光",
        "低": "干爽无油，哑光质感",
    },
    "堆叠感": {
        "高": "食材分量满，多层次堆叠，略溢出盘沿",
        "中": "适度堆叠，层次分明",
        "低": "摆盘简洁，食材稀疏",
    },
    "配料感": {
        "高": "芝麻 + 葱花 + 辣椒 + 八角等多种配料点缀",
        "中": "2-3 种配料点缀",
        "低": "少量或无配料",
    },
    "热气感": {
        "高": "明显蒸汽上升，视觉感知温度感",
        "中": "微蒸汽，不遮挡主体",
        "低": "无蒸汽，冷食表现",
    },
    "拉丝感": {
        "高": "筷子 / 勺子提起时明显拉丝动态定格",
        "中": "适度拉丝",
        "低": "无拉丝表现",
    },
}


def render_appetite(params):
    """dict → prompt 文本行"""
    lines = []
    for k in APPETITE_KEYS:
        if k in params:
            level = params[k]
            desc = APPETITE_MAP.get(k, {}).get(level, level)
            lines.append(f"- {k}：{level}（{desc}）")
    return "\n".join(lines)


